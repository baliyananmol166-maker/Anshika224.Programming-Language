// Assignment operator in c
#include <stdio.h>
int main() {
    int a=10; // assignment operator
    printf("The value of a is: %d\n",a);
    a+=5; // addition assignment operator
    printf("The value of a is: %d\n",a);
    a-=5; // subtraction assignment operator
    printf("The value of a is: %d\n",a);
    a*=5; // multiplication assignment operator
    printf("The value of a is: %d\n",a);
    a/=5; // division assignment operator
    printf("The value of a is: %d\n",a);
    a%=5; // modulus assignment operator
    printf("The value of a is: %d\n",a);
    return 0;
}