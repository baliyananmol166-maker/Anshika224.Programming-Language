//Arithmetic Operators in C
#include <stdio.h>
int main() {
    int a= 10;
    int b=5;
    printf("The value of a+b is: %d\n",a+b); // addition operator
    printf("The value of a-b is: %d\n", a-b); // subtraction operator
    printf("The value of a*b is: %d\n", a*b); // multiplication operator
    printf("The value of a/b is: %d\n", a/b); // division operator
    printf("The value of a%%b is: %d\n", a%b); // modulus operator
    return 0;
}