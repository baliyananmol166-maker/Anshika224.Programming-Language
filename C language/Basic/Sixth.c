// Relational operator in C
#include <stdio.h>
int main() {
    int a=10;
    int b=5;
    printf("The value of a==b is: %d\n", a==b); //equality operator
    printf("The value of a!=b is: %d\n", a!=b); //not equal operator
    printf("The value of a>b is: %d\n", a>b); //greater than operator
    printf("The value of a<b is: %d\n", a<b); //less than operator
    printf("The value of a>=b is: %d\n", a>=b); //greater than or equal to operator
    printf("The value of a<=b is: %d\n", a<=b); //less than or equal to operator
    return 0;
}